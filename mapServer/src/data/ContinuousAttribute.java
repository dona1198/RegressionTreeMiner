package data;

/**
 * Classe per definire attributo di tipo Continuo (valori numerici)
 * @author Donato Lerario
 *
 */
public class ContinuousAttribute extends Attribute{
	
	/**
	 * Invoca il costruttore della super-classe
	 * @param name valori per nome simbolico dell'attributo
	 * @param index identificativo numerico dell'attributo
	 */
	ContinuousAttribute(String name, int index) {
		super(name, index);
	}
}
